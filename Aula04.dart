void main() {
  /*
     Operadores Relacionais:
    
    == (igual a)
    
   != (diferente)
   
   > (Maior que)
   
   < (Menor que)
   
   >= (Maior ou igual)
   
   <= (Menor ou igual)
    
   */

  print(5 == 5);

  print(5 != 5);

  print(5 > 5);

  print(5 < 5);

  print(5 >= 5);

  print(5 <= 5);
}
