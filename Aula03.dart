void main() {
  // Operadores Aritméticos Básicos:

  var num1 = 10;
  var num2 = 2;

  var resultado = num1 + num2;

  print("O resultado da soma dos números é: $resultado");

  resultado = num1 - num2;

  print("O resultado da subtração dos números é: $resultado");

  resultado = num1 * num2;

  print("O resultado da multiplicação dos números é: $resultado");

  var resultado2 = num1 / num2;

  print("O resultado da divisão dos números é: $resultado2");

  print("==========================================");

  // Operador de Incremento:

  var numero = 10;
  numero += 15;
  print(numero);

  var numero3 = 10;
  numero3 -= 15;
  print(numero3);

  var numero4 = 20;
  numero4 *= 10;
  print(numero4);

  double numero5 = 100;
  numero5 /= 2;
  print(numero5);

  // Adicionando 1:

  var numero6 = 10;
  numero6++;
  print(numero6);
}
